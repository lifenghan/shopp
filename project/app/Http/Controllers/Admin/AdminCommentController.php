        $comment=DB::table("comment")->join("user","comment.user_id","=","user.id")->join("product","comment.product_id","=","product.id")->select("comment.id","user.username as uname","product.name as pname","comment.content","comment.time")->get();
        // dd($comment);
        //加载后台评价模板
        return view("Admin.Comment.index",['comment'=>$comment]);
    }

