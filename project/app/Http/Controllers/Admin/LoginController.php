        $info = $request->except(["_token"]);
        $name = $request->input("name");
        $password = $request->input("password");
//        $data =  DB::table("admin_users")->where($info)->first();
        $data =  DB::table("admin_users")->where("name","=",$name)->first();
        if($data){
            //名字匹配
            if($password ==$data->password){
                //密码匹配
                session(['admin'=>$data]);
                return redirect("/admin")->with("info","登录成功");
            }else{
                //密码不匹配
                return back()->with("pwd_error","亲,密码有误哦");
            }
        }else{
            //名字不匹配
            return back()->with("name_error","没有这个用户哦亲");
        }
    }

